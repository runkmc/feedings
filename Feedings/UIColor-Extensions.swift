import UIKit

extension UIColor {
  
  static public var feedingsOrange: UIColor {
    return UIColor.init(hex: 0xF86400FF)
  }
  
  static public var feedingsBlue: UIColor {
    return UIColor.init(hex: 0x00BCF8FF)
  }
}
